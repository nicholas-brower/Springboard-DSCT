@dataclass
class Feature:
    '''A class facilitating organization of features of a dataset.    
    '''
    name: str
    sql_type: str = field(default_factory=str)
    category: str = field(init=False, default_factory=str)
    groups: set[str] = field(default_factory=set)
    col: str = field(init=False)
    
    def __post_init__(self):
        self.groups.add('all')
        if self.name == 'S_2':
            self.category = 'date'
        elif any([self.name.startswith('_') for _ in 'DSPBRct']):
            self.category = {
                'D': 'delinquency', 'S': 'spend', 'P': 'payment', 
                'B': 'balance', 'R': 'risk', 'c': 'id', 
                't': 'target'
            }[self.name[0]]
            self.groups.add(self.category)
        if self.name[0] in 'DSPBR':
            self.groups.add('train')
        self.col = self.name
        if self not in FEATURES:
            FEATURES.append(self)

    def in_group(self, group_name: str) -> bool:
        return group_name in self.groups
    def in_groups(self, group_names: list[str]) ->bool:
        return all([self.in_group(g) for g in group_names])

@dataclass
class FeatureGroup:
    name: str
    features: list[Feature] = field(default_factory=list)
    
    def __post_init_(self):
        for feature in FEATURES:
            if self.name in feature.groups:
                if feature not in self.features:
                    self.features.append(feature)
    
    def sorted(self) -> list:
        return sorted(self.features, key=col)
    
    def cols(self, alias: str='') -> str:
        '''Return a list of strings representing features in group'''
        return [feature.col for feature in self.features]

